#!/usr/bin/env node
/**
 * qa/tools/sync-codeowners.mjs
 *
 * TC frontmatter'larındaki `owner:` alanlarından suite-bazlı CODEOWNERS
 * üretir. Repo köküne `.github/CODEOWNERS`'a (qa/ bölümü) yazar veya
 * stdout'a basar.
 *
 * Suite içindeki tüm TC'ler aynı owner'a aitse, o suite klasörüne tek owner
 * yazılır. Çoklu owner varsa hepsi listelenir (PR review için).
 *
 * Usage:
 *   node qa/tools/sync-codeowners.mjs              # stdout (dry-run)
 *   node qa/tools/sync-codeowners.mjs --write      # .github/CODEOWNERS'a yaz/update
 */

import { readFile, writeFile, stat } from "node:fs/promises";
import path from "node:path";
import { parseArgs } from "node:util";
import { REPO_ROOT, loadAllTestCases, loadAllSuites } from "./lib/files.mjs";

const { values: args } = parseArgs({
  options: { write: { type: "boolean", default: false } },
});

const tcs = (await loadAllTestCases()).filter((t) => t.data);
const suites = (await loadAllSuites()).filter((s) => s.data);

const ownersBySuite = {};
for (const t of tcs) {
  const suite = t.data.suite;
  ownersBySuite[suite] ??= new Set();
  if (t.data.owner) ownersBySuite[suite].add(t.data.owner);
}

const suiteMeta = {};
for (const s of suites) {
  const folder = path.basename(path.dirname(s.file));
  suiteMeta[folder] = { id: s.data.id, owner: s.data.owner };
}

const sortedSuites = Object.keys(ownersBySuite).sort();

const generatedSection = [
  "# ─── qa/ section — AUTO-GENERATED by qa/tools/sync-codeowners.mjs ───",
  "# Bu blok TC frontmatter `owner:` alanlarından üretilir. Manuel düzenleme yapma;",
  "# TC dosyalarında `owner: \"@username\"` alanını değiştir ve sync'i tekrar çalıştır.",
  "",
  "# Strateji ve konvansiyonlar",
  "qa/CONVENTIONS.md          @qa-leads",
  "qa/README.md               @qa-leads",
  "qa/strategy/               @qa-leads",
  "qa/INSTALL.md              @qa-leads",
  "",
  "# Tooling",
  "qa/tools/                  @qa-leads",
  "qa/.github-CODEOWNERS      @qa-leads",
  "",
  "# Plans + Runs",
  "qa/plans/                  @qa-leads",
  "qa/runs/                   @qa-leads",
  "qa/milestones/             @qa-leads",
  "",
  "# Cross-cutting",
  "qa/templates/              @qa-leads",
  "qa/test-design/            @qa-leads",
  "qa/reporting/              @qa-leads",
  "qa/requirements/           @qa-leads",
  "qa/shared/                 @qa-leads",
  "",
  "# Suite-bazlı sahiplik (TC frontmatter'larından üretildi)",
];

for (const suite of sortedSuites) {
  const owners = [...ownersBySuite[suite]].filter((o) => o && o !== "@unassigned").sort();
  const suiteOwner = suiteMeta[suite]?.owner;
  if (suiteOwner && !owners.includes(suiteOwner)) owners.unshift(suiteOwner);

  const ownerStr = owners.length > 0 ? owners.join(" ") : "@qa-leads  # ⚠ no owner specified";
  generatedSection.push(`qa/cases/${suite}/${" ".repeat(Math.max(1, 24 - suite.length))}${ownerStr}`);
}

generatedSection.push("");
generatedSection.push("# ─── end of qa/ section ───");

const generatedText = generatedSection.join("\n");

const codeOwnersPath = path.join(REPO_ROOT, ".github", "CODEOWNERS");
let existing = "";
try {
  existing = await readFile(codeOwnersPath, "utf8");
} catch {
  // first time
}

const markerStart = "# ─── qa/ section — AUTO-GENERATED";
const markerEnd = "# ─── end of qa/ section ───";

let newContent;
if (existing.includes(markerStart) && existing.includes(markerEnd)) {
  const before = existing.slice(0, existing.indexOf(markerStart));
  const afterIdx = existing.indexOf(markerEnd) + markerEnd.length;
  const after = existing.slice(afterIdx);
  newContent = before + generatedText + after;
} else if (existing.trim()) {
  newContent = existing.trimEnd() + "\n\n" + generatedText + "\n";
} else {
  newContent = generatedText + "\n";
}

if (args.write) {
  await writeFile(codeOwnersPath, newContent, "utf8");
  console.log(`✓ ${path.relative(REPO_ROOT, codeOwnersPath)} updated`);
  console.log(`  ${sortedSuites.length} suite, ${tcs.length} TC analyzed`);
} else {
  console.log(generatedText);
  console.log("\n# (dry-run — use --write to apply)");
}
